CREATE OR REPLACE FUNCTION pg_temp.next_may_transaction_no()
RETURNS TEXT
LANGUAGE plpgsql
AS $function$
DECLARE
    v_prefix TEXT;
    v_last_no TEXT;
    v_sequence BIGINT;
BEGIN
    v_prefix := 'IT-' || to_char(clock_timestamp(), 'YYYYMMDDHH24MISS') || '-';
    SELECT transaction_no
      INTO v_last_no
      FROM inventory_transactions
     WHERE transaction_no LIKE v_prefix || '%'
     ORDER BY transaction_no DESC
     LIMIT 1;
    v_sequence := COALESCE(NULLIF(replace(v_last_no, v_prefix, ''), '')::BIGINT, 0) + 1;
    RETURN v_prefix || lpad(v_sequence::TEXT, 3, '0');
END
$function$;

CREATE OR REPLACE FUNCTION pg_temp.next_may_movement_no(p_movement_date TEXT)
RETURNS TEXT
LANGUAGE plpgsql
AS $function$
DECLARE
    v_prefix TEXT;
    v_last_no TEXT;
    v_sequence BIGINT;
BEGIN
    v_prefix := 'FM-' || replace(p_movement_date, '-', '') || '-';
    SELECT related_order_no
      INTO v_last_no
      FROM inventory_transactions
     WHERE related_order_no LIKE v_prefix || '%'
     ORDER BY related_order_no DESC
     LIMIT 1;
    v_sequence := COALESCE(NULLIF(replace(v_last_no, v_prefix, ''), '')::BIGINT, 0) + 1;
    RETURN v_prefix || lpad(v_sequence::TEXT, 3, '0');
END
$function$;

CREATE OR REPLACE FUNCTION pg_temp.post_may_manual_movement(
    p_movement_date TEXT,
    p_material_code TEXT,
    p_movement_type TEXT,
    p_quantity DOUBLE PRECISION,
    p_remark TEXT
)
RETURNS TEXT
LANGUAGE plpgsql
AS $function$
DECLARE
    v_warehouse_id BIGINT;
    v_admin_id BIGINT;
    v_admin_name TEXT;
    v_material_id BIGINT;
    v_material_name TEXT;
    v_before_qty DOUBLE PRECISION := 0;
    v_after_qty DOUBLE PRECISION;
    v_avg_cost BIGINT := 0;
    v_new_cost BIGINT := 0;
    v_movement_no TEXT;
    v_transaction_type TEXT;
    v_transaction_quantity DOUBLE PRECISION;
BEGIN
    IF p_movement_type NOT IN ('in', 'out') OR p_quantity <= 0 THEN
        RAISE EXCEPTION '自由出入库参数异常: % / %', p_movement_type, p_quantity;
    END IF;

    SELECT warehouse_id, admin_id, admin_name
      INTO STRICT v_warehouse_id, v_admin_id, v_admin_name
      FROM may_2026_import_context;
    SELECT id, name
      INTO STRICT v_material_id, v_material_name
      FROM materials
     WHERE code = p_material_code AND is_enabled = TRUE;

    SELECT quantity, avg_cost
      INTO v_before_qty, v_avg_cost
      FROM inventory
     WHERE material_id = v_material_id AND warehouse_id = v_warehouse_id
     FOR UPDATE;
    IF NOT FOUND THEN
        v_before_qty := 0;
        v_avg_cost := 0;
    END IF;

    v_movement_no := pg_temp.next_may_movement_no(p_movement_date);
    IF p_movement_type = 'in' THEN
        v_after_qty := v_before_qty + p_quantity;
        v_new_cost := CASE
            WHEN v_after_qty > 0 THEN round((v_before_qty * v_avg_cost::DOUBLE PRECISION) / v_after_qty)::BIGINT
            ELSE 0
        END;
        INSERT INTO inventory (material_id, warehouse_id, quantity, avg_cost, last_in_date, updated_at)
        VALUES (v_material_id, v_warehouse_id, v_after_qty, v_new_cost, p_movement_date, NOW())
        ON CONFLICT (material_id, warehouse_id) DO UPDATE SET
            quantity = excluded.quantity,
            avg_cost = excluded.avg_cost,
            last_in_date = excluded.last_in_date,
            updated_at = NOW();
        v_transaction_type := 'other_in';
        v_transaction_quantity := p_quantity;
        v_avg_cost := 0;
    ELSE
        IF v_before_qty < p_quantity THEN
            RAISE EXCEPTION '库存不足: 物料 %, 当前 %, 出库 %', p_material_code, v_before_qty, p_quantity;
        END IF;
        v_after_qty := v_before_qty - p_quantity;
        UPDATE inventory
           SET quantity = v_after_qty,
               last_out_date = p_movement_date,
               updated_at = NOW()
         WHERE material_id = v_material_id AND warehouse_id = v_warehouse_id;
        v_transaction_type := 'other_out';
        v_transaction_quantity := -p_quantity;
    END IF;

    INSERT INTO inventory_transactions (
        transaction_no, transaction_date, material_id, warehouse_id, transaction_type,
        quantity, before_qty, after_qty, unit_cost, source_type, related_order_no,
        operator_user_id, operator_name, remark
    ) VALUES (
        pg_temp.next_may_transaction_no(), p_movement_date, v_material_id, v_warehouse_id,
        v_transaction_type, v_transaction_quantity, v_before_qty, v_after_qty, v_avg_cost,
        'manual_stock_movement', v_movement_no, v_admin_id, v_admin_name, p_remark
    );

    INSERT INTO operation_logs (
        module, action, target_type, target_id, target_no, detail,
        operator_user_id, operator_name_snapshot
    ) VALUES (
        'inventory', 'manual_' || p_movement_type, 'manual_stock_movement', NULL, v_movement_no,
        '自由' || CASE p_movement_type WHEN 'in' THEN '入库 ' ELSE '出库 ' END ||
            v_movement_no || '，物料 ' || v_material_name || '，数量 ' || p_quantity::TEXT ||
            '；' || p_remark,
        v_admin_id, v_admin_name
    );

    RETURN v_movement_no;
END
$function$;

DO $opening$
DECLARE
    v_warehouse_id BIGINT;
    v_admin_id BIGINT;
    v_admin_name TEXT;
    v_material_id BIGINT;
    v_before_qty DOUBLE PRECISION;
