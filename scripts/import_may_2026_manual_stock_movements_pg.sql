        SELECT COALESCE(quantity, 0)
          INTO v_before_qty
          FROM inventory
         WHERE material_id = v_material_id AND warehouse_id = v_warehouse_id
         FOR UPDATE;
        IF NOT FOUND THEN
            v_before_qty := 0;
        END IF;
        v_after_qty := v_before_qty + item.quantity;

        INSERT INTO inventory (material_id, warehouse_id, quantity, avg_cost, last_in_date, updated_at)
        VALUES (v_material_id, v_warehouse_id, v_after_qty, 0, '2026-05-01', NOW())
        ON CONFLICT (material_id, warehouse_id) DO UPDATE SET
            quantity = excluded.quantity,
            avg_cost = excluded.avg_cost,
            last_in_date = excluded.last_in_date,
