        INSERT INTO inventory_transactions (
            transaction_no, transaction_date, material_id, warehouse_id, transaction_type,
            quantity, before_qty, after_qty, unit_cost, source_type, related_order_no,
            operator_user_id, operator_name, remark
        ) VALUES (
            pg_temp.next_may_transaction_no(), '2026-05-01', v_material_id, v_warehouse_id,
            'other_in', item.quantity, v_before_qty, v_after_qty, 0,
