          FROM inventory_transactions it
          JOIN source_materials sm ON sm.id = it.material_id
         WHERE it.warehouse_id = v_warehouse_id
    ) THEN
        RAISE EXCEPTION '目标原材料仓已存在本次物料库存或流水，拒绝叠加导入期初';
    END IF;

