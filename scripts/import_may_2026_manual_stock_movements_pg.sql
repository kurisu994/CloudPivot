    ) VALUES (
        'inventory', 'import_initial', 'initial_inventory', NULL, 'INIT',
        '[酉昌2026-05导入] 导入5月期初库存 354 条，来源 scripts/酉昌进销存明细.xlsx',
        v_admin_id, v_admin_name
    );
END
$opening$;

DO $daily$
DECLARE
    v_warehouse_id BIGINT;
    v_current_qty DOUBLE PRECISION;
    v_gap DOUBLE PRECISION;
    v_material_id BIGINT;
    item RECORD;
BEGIN
    SELECT warehouse_id INTO STRICT v_warehouse_id FROM may_2026_import_context;
    FOR item IN SELECT * FROM may_2026_daily_movements ORDER BY source_order LOOP
        IF item.movement_type = 'out' THEN
            SELECT id INTO STRICT v_material_id FROM materials WHERE code = item.material_code AND is_enabled = TRUE;
            SELECT COALESCE(quantity, 0)
              INTO v_current_qty
              FROM inventory
             WHERE material_id = v_material_id AND warehouse_id = v_warehouse_id
             FOR UPDATE;
            IF NOT FOUND THEN
                v_current_qty := 0;
            END IF;
            IF v_current_qty < item.quantity THEN
                v_gap := item.quantity - v_current_qty;
                PERFORM pg_temp.post_may_manual_movement(
                    item.movement_date, item.material_code, 'in', v_gap,
                    '[酉昌2026-05导入] 源表负库存补足；来源行=' || item.source_row::TEXT
                );
            END IF;
        END IF;
        PERFORM pg_temp.post_may_manual_movement(
            item.movement_date, item.material_code, item.movement_type, item.quantity,
            '[酉昌2026-05导入] 源表每日' || CASE item.movement_type WHEN 'in' THEN '入库' ELSE '出库' END ||
            '；来源行=' || item.source_row::TEXT
        );
    END LOOP;
END
$daily$;

DO $verification$
DECLARE
    v_initial_count BIGINT;
    v_manual_count BIGINT;
    v_supplement_count BIGINT;
    v_supplement_quantity DOUBLE PRECISION;
BEGIN
    SELECT COUNT(*) INTO v_initial_count
      FROM inventory_transactions
     WHERE source_type = 'initial_inventory' AND remark LIKE '%[酉昌2026-05导入]%';
    SELECT COUNT(*) INTO v_manual_count
      FROM inventory_transactions
     WHERE source_type = 'manual_stock_movement' AND remark LIKE '%[酉昌2026-05导入]%';
    SELECT COUNT(*), COALESCE(SUM(quantity), 0)
      INTO v_supplement_count, v_supplement_quantity
      FROM inventory_transactions
     WHERE transaction_type = 'other_in'
       AND source_type = 'manual_stock_movement'
       AND remark LIKE '%[酉昌2026-05导入] 源表负库存补足%';

    IF v_initial_count <> 354
       OR v_manual_count <> 463
       OR v_supplement_count <> 22
       OR v_supplement_quantity <> 1200 THEN
        RAISE EXCEPTION '导入写入数校验失败: 期初 %, 日常 %, 补足 %/%',
            v_initial_count, v_manual_count, v_supplement_count, v_supplement_quantity;
    END IF;

    IF EXISTS (
        SELECT 1
          FROM inventory inv
          JOIN materials m ON m.id = inv.material_id
          CROSS JOIN may_2026_import_context ctx
         WHERE m.code IN (
             SELECT material_code FROM may_2026_opening_stock
             UNION
             SELECT material_code FROM may_2026_daily_movements
         )
           AND inv.warehouse_id = ctx.warehouse_id
           AND inv.quantity < 0
    ) THEN
        RAISE EXCEPTION '导入后存在负库存，事务已终止';
    END IF;
END
