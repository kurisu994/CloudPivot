    END IF;

    SELECT dw.warehouse_id
      INTO v_warehouse_id
      FROM default_warehouses dw
      JOIN warehouses w ON w.id = dw.warehouse_id AND w.is_enabled = TRUE
     WHERE dw.material_type = 'raw';
